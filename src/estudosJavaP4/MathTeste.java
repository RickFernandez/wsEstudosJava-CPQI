package estudosJavaP4;

public class MathTeste {

	public static void main(String[] args) {
		
		System.out.println(Math.sin(10)); //seno
		System.out.println(Math.cos(10)); //cosseno
		System.out.println(Math.tan(10)); //tangente
		System.out.println(Math.pow(4, 2)); //potência (4²)
		System.out.println(Math.sqrt(16)); //raiz quadrada
		System.out.println(Math.cbrt(16)); //raiz cubica
		System.out.println(Math.hypot(6, 8)); //hypotenusa
		
		
	}
}
